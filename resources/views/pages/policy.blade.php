        <ol type="a">
          <li>Data Protection Officer function is established as a position directly subordinated and in direct reporting line to Top Management;</li><br>
          <li>Data Protection Officer function is not subject to conflicting interests;</li><br>
          <li>Company involves Data Protection Officer properly and in a timely manner in all issues which relate to the protection of Personal Data;</li><br>
        </ol>
      </li><br>
      <li> Company shall:<br><br>
        <ol type="a">
          <li>publish the contact details of the Data Protection Officer to Data Subjects and also internally on Company’s intranet, internal telephone directory, and organizational charts to ensure that his or her existence and function is known within the organization;</li><br>
          <li>communicate the contact details to the competent supervisory authority;</li><br>
          <li>make sure that Data Protection Officer is invited to participate regularly in meetings of senior management;</li><br>
          <li>always give a due weight to Data Protection Officer´s opinion. In case of disagreement it is important to document the reasons for not following the Data protection Officer’s advice;</li><br>
          <li>promptly and without undue delay consult the Data Protection Officer once a data breach or other incident has occurred;</li><br>
          <li>support Data Protection Officer by ‘providing resources necessary to carry out his/her tasks and access to Personal Data and processing operations, and to maintain his or her expert knowledge’.</li><br>
          <li>provide the Data Protection Officer with regular training. Data Protection Officer will be given the opportunity to stay up to date with regard to developments within data protection. The aim should be to constantly increase the level of expertise of Data Protection Officer, he/she should be encouraged to participate in training courses on data protection and other forms of professional development, such as participation in privacy fora, workshops, etc.;</li><br>
          <li>ensure that the Data Protection Officer ‘does not receive any instructions regarding the exercise of his or her tasks’.</li><br>
        </ol>
      </li><br>
      <li>Data Protection Officer shall be bound by secrecy or confidentiality concerning the performance of his or her tasks.</li><br>
