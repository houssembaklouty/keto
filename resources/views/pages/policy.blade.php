            When informing about the logic the automated decision-making is based on, it is not necessary to disclose in detail the algorithm, formula or business rationale applied (this information need not to be so in-depth as to compromise the Company’s business secrets). It is suggested to present the operation of automated decision-making by using examples.
          </li><br>
          <li>
            <u>revision of automated decisions</u>: The Data Subject subjected to an automated decision-making is entitled to request human intervention from the Company, to make his/her position known and submit an objection against that decision. This right to challenge does not entitle the Data Subject to force the conclusion of the contract, but to dispute the decision based on automated data processing leading to the rejection of concluding the contract.<br><br>
            The Data Subject must be granted the opportunity in any case to avail of his/her right to make an objection.
          </li><br>
          <li><u>the right to object</u>: if the legal basis for Processing is a legitimate interest of Company, the Data Subject is entitled to the right to object. It follows from the right to object that the Company must examine whether this objection is justified (i.e. whether the Data Subject’s interests override the Company’s interests) and decide on the objection.</li><br>
        </ol>
      </li><br>
    </ol>

    <h2>Company personnel recognises and knows how to deal with a request from Data Subjects</h2>

    <ol start="32">
      <li>EU Data Protection Laws impose that any Data Subject request is responded as soon as possible but no later than 30 days from receipt.</li><br>
      <li>Company employees will treat with the utmost importance all enquiries from Data Subjects about the Processing activity.</li><br>
      <li>In all cases, Company employees will inform Data Subjects that they may submit a formal request and/or a complaint to the designated address, representing the contact details of the DPO appointed by the Company.</li><br>
