                                cétose. Alors qu'est-ce que le BHB exactement? Le BHB (Beta ‐ Hydroxybutyrate) est un
                                composé organique et est considéré comme l'un des corps cétoniques «physiologiques»
                                produits et brûlés dans nos cellules.
